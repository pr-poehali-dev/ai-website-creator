const STOP_TOKENS = new Set([
  'и','в','на','с','для','по','из','о','а','но','что','как','это','то',
  'сайт','хочу','нужен','нужно','сделай','создай','мне','под','от','до',
  'бы','же','ли','или','за','при','без','над','перед','после','через',
  'которые','которого','которому','который','которая','которым','которых',
  'очень','просто','быстро','хорошо','красиво','удобно','круто','классно',
  'чтобы','тоже','ещё','еще','уже','вот','так','там','тут','здесь',
  'мой','моя','моё','мое','моих','моим','мою','твой','наш','ваш','свой',
  'один','два','три','первый','второй','новый','старый','большой','малый',
  'быть','есть','стать','делать','иметь','мочь','хотеть','знать','видеть',
  'можно','нельзя','надо','нужно','должен','будет','было','были',
]);

function tokenizeAndStem(text: string): string[] {
  return text
    .toLowerCase()
    .replace(/ё/g, 'е')
    .split(/[\s,.!?:;()[\]{}«»"'`\-—–/\\|@#$%^&*+=<>~]+/)
    .filter(w => w.length > 1)
    .map(snowballRu)
    .filter(stem => stem.length > 1 && !STOP_TOKENS.has(stem));
}

