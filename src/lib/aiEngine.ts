// ═══════════════════════════════════════════════════════════════════════════════
// SNOWBALL RUSSIAN STEMMER — алгоритм Портера для русского языка
// Источник: https://snowballstem.org/algorithms/russian/stemmer.html
// Понимает ВСЕ грамматические формы: падежи, спряжения, причастия, деепричастия,
// превосходные/сравнительные степени, уменьшительные, ласкательные и т.д.
// ═══════════════════════════════════════════════════════════════════════════════

const VOWELS = new Set('аеиоуыэюяё');

function isVowel(ch: string) { return VOWELS.has(ch); }

/** Найти RV: всё после первой гласной */
function rv(word: string): number {
  for (let i = 0; i < word.length; i++) {
    if (isVowel(word[i])) return i + 1;
  }
  return word.length;
}

/** Найти R1: после первой согласной+гласной */
function r1(word: string): number {
  let foundVowel = false;
  for (let i = 0; i < word.length - 1; i++) {
    if (isVowel(word[i])) { foundVowel = true; }
    else if (foundVowel) { return i + 1; }
  }
  return word.length;
}

function stripSuffix(word: string, suffixes: string[], minLen = 3): string | null {
  for (const s of suffixes) {
    if (word.endsWith(s) && word.length - s.length >= minLen) {
      return word.slice(0, word.length - s.length);
    }
  }
  return null;
}

// Шаг 1: перфективные глаголы
const PERFECTIVE_GERUND_1 = ['вшись','вши','вшего','вшему','вших','вшим','вшей','вше'];
const PERFECTIVE_GERUND_2 = ['ившись','ывшись','ивши','ывши'];

// Рефлексивы
const REFLEXIVE = ['ся','сь'];

// Прилагательные
const ADJECTIVE = [
  'ими','ыми','его','ого','ему','ому','ее','ое','ие','ые',
  'ей','ой','ий','ый','ая','яя','ую','юю',
  'ими','ыми','их','ых',
];

// Причастия
const PARTICIPLE_1 = ['ем','нн','вш','ющ','щ'];
const PARTICIPLE_2 = ['ивш','ывш','ующ'];

// Глагольные окончания
const VERB_1 = [
  'ешь','ишь','ете','ите','ют','ат','ят','ет','ит',
  'ла','ло','ли','л','й','ть','ти','чь',
  'ем','им','ешь','ишь',
];
const VERB_2 = [
  'овала','овало','овали','овал','овать','овано',
  'уйте','уют','уешь','уете','уем','уй',
  'ивала','ивало','ивали','ивал','ивать',
  'ываю','ывает','ывают','ывала','ывал','ывать',
];

// Существительные
const NOUN = [
  'ами','ями','ием','ием','ией','ией',
  'ам','ям','ом','ем','ём','ем',
  'ах','ях','ий','ия','ие','ии',
  'ей','ей','ою','ею',
  'ов','ев','ев',
  'ах','ях','ом','ем',
  'ам','ям','ах','ях',
  'ой','ой','ей','ей',
  'ию','ию','ем','ем',
  'ах','ях','ие','ие',
  'ей','ий','ой','ый',
  'а','е','и','о','у','ы','ь','я','ю','э',
];

// Деривационные суффиксы
const DERIVATIONAL = [
  'ост','ость','ем','нн','ть','ейш','ейше',
];

function snowballRu(token: string): string {
  let w = token.toLowerCase();
  // Нормализуем ё → е для унификации
  w = w.replace(/ё/g, 'е');
  if (w.length < 3) return w;

  const rvPos = rv(w);
  const r1Pos = r1(w);

  // Шаг 1: убираем перфективные глаголы
  let stem = stripSuffix(w, PERFECTIVE_GERUND_2, rvPos);
  if (!stem) {
    // только если не начинается на а/я
    const noAYa = !['а','я'].some(c => w.slice(rvPos).startsWith(c));
    if (noAYa) stem = stripSuffix(w, PERFECTIVE_GERUND_1, rvPos);
  }

  if (stem) { w = stem; }
  else {
    // Убираем рефлексив
    const refl = stripSuffix(w, REFLEXIVE, rvPos);
    if (refl) w = refl;

    // Пробуем: прилагательное + причастие / только прилагательное
    const adjStem = stripSuffix(w, ADJECTIVE, rvPos);
    if (adjStem) {
      const partStem = stripSuffix(adjStem, PARTICIPLE_2, rvPos)
        || stripSuffix(adjStem, PARTICIPLE_1, rvPos);
      w = partStem || adjStem;
    } else {
      // Глагол
      const verbStem = stripSuffix(w, VERB_2, rvPos) || stripSuffix(w, VERB_1, rvPos);
      if (verbStem) { w = verbStem; }
      else {
        // Существительное
        const nounStem = stripSuffix(w, NOUN, rvPos);
        if (nounStem) w = nounStem;
      }
    }
  }

  // Шаг 2: убираем -и в конце (для rvPos)
  if (w.length > rvPos && w.endsWith('и')) w = w.slice(0, -1);

  // Шаг 3: деривационный
  if (w.length > r1Pos) {
    const derStem = stripSuffix(w, DERIVATIONAL, r1Pos);
    if (derStem) w = derStem;
  }

  // Шаг 4: удаляем ь, нн→н, финальные ейш/ейше
  if (w.endsWith('ь')) w = w.slice(0, -1);
  if (w.endsWith('нн')) w = w.slice(0, -1);
  if (w.endsWith('ейш') || w.endsWith('ейше')) w = w.replace(/ейш(е)?$/, '');

  return w.length >= 2 ? w : token.toLowerCase().replace(/ё/g, 'е');
}

