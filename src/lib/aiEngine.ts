// КОНЦЕПТЫ: хранятся в стемированном виде (уже прогнаны через snowballRu)
// Это позволяет сопоставлять ЛЮБУЮ форму слова с любым концептом
// ═══════════════════════════════════════════════════════════════════════════════

/** Стемируем концепт один раз при загрузке */
function sc(word: string): string {
  return snowballRu(word.toLowerCase().replace(/ё/g, 'е'));
}

