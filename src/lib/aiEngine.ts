const STOP_TOKENS = new Set([
  'и','в','на','с','для','по','из','о','а','но','что','как','это','то',
  'сайт','хочу','нужен','нужно','сделай','создай','мне','под','от','до',
  'бы','же','ли','или','за','при','без','над','перед','после','через',
  'который','которая','которые','которого','очень','просто','быстро',
  'хорошо','красиво','удобно','круто','классно','чтобы','тоже','ещё',
  'еще','уже','вот','так','там','тут','здесь','мой','моя','мою','наш','ваш',
]);

function extractName(prompt: string): string {
  const quoted = prompt.match(/[«""]([^»""]+)[»""]/);
  if (quoted) return quoted[1];
  const words = prompt.split(/[\s,.!?:;()«»"'-]+/);
  for (let i = 1; i < words.length; i++) {
    const w = words[i];
    if (w.length > 2 && /^[А-ЯЁA-Z]/.test(w) && !STOP_TOKENS.has(w.toLowerCase())) return w;
  }
  const sig = words.find((w) => w.length > 3 && !STOP_TOKENS.has(w.toLowerCase()));
  if (sig) return sig[0].toUpperCase() + sig.slice(1).toLowerCase();
  return 'Мой Сайт';
}

function detectSiteType(stems: string[], p: string): string {
  if (/лендинг|landing|одностранич|посадочн/.test(p)) return 'Лендинг';
  if (stems.some(t => ['магазин','шоп','каталог','корзин'].some(c => t.startsWith(sc(c))))) return 'Интернет-магазин';
  if (stems.some(t => ['портфолио','галере'].some(c => t.startsWith(sc(c))))) return 'Портфолио';
  if (/блог|статьи|новост/.test(p)) return 'Блог';
  if (/визитк/.test(p)) return 'Сайт-визитка';
  return 'Многостраничный сайт';
}

