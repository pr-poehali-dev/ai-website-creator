export function analyzePrompt(prompt: string, modelId: string): GeneratedSite {
  const p = prompt.toLowerCase().replace(/ё/g, 'е').trim();
  const stems = tokenizeAndStem(prompt);
  const reasoning: string[] = [];

  const wordCount = prompt.split(/\s+/).filter(Boolean).length;
  reasoning.push(`Слов в запросе: ${wordCount} → стемов: ${stems.length}`);

  // Скоринг по пересечению стем с концептными стемами
  let best = DEFAULT_INDUSTRY;
  let bestScore = 0;
  let bestMatches: string[] = [];

  for (const ind of INDUSTRIES) {
    const matches: string[] = [];
    for (const stem of stems) {
      if (ind.stems.has(stem)) {
        if (!matches.includes(stem)) matches.push(stem);
      } else {
        // Частичное совпадение: stem начинается с концептного корня (min 3 символа)
        for (const concept of ind.stems) {
          if (concept.length >= 3 && stem.length >= 3
            && (stem.startsWith(concept) || concept.startsWith(stem))) {
            if (!matches.includes(stem)) matches.push(stem);
            break;
          }
        }
      }
    }
    if (matches.length > bestScore) {
      bestScore = matches.length;
      best = ind;
      bestMatches = matches;
    }
  }

  if (bestScore > 0) {
    reasoning.push(`Тематика: «${best.industry}» (${bestMatches.length} совп.: ${bestMatches.slice(0, 3).join(', ')})`);
  } else {
    reasoning.push('Тематика не определена — применяю универсальный шаблон');
  }

  const siteType = detectSiteType(stems, p);
  reasoning.push(`Тип: ${siteType}`);

  // Палитра по цветовым токенам
  let paletteIdx = best.palette;
  const colorMap: [RegExp, number][] = [
    [/синий|голубой|лазурный|морской|небесный|бирюзовый|аквамарин/, 1],
    [/оранжевый|янтарный|золотой|тёплый|теплый|медовый|персиков/, 2],
    [/зелёный|зеленый|эко|природный|травяной|лесной|оливков/, 3],
    [/розовый|нежный|коралловый|пудровый|малиновый|фламинго/, 4],
    [/фиолетовый|сиреневый|лавандовый|космос|тёмный|темный|ночной/, 5],
    [/жёлтый|желтый|лимонный|солнечный|яркий|лимон/, 6],
    [/серый|серебряный|металлик|стальной|графитовый/, 7],
  ];
  for (const [re, idx] of colorMap) {
    if (re.test(p)) { paletteIdx = idx; break; }
  }
  reasoning.push(`Палитра: «${PALETTES[paletteIdx].name}»`);

  const model = AI_MODELS.find((m) => m.id === modelId) || AI_MODELS[0];
  const bonus = modelId === 'nebula-ultra' ? 12 : modelId === 'nebula-pro' ? 6 : 2;
  const confidence = Math.min(99.9, 70 + bestScore * 7 + bonus);
  reasoning.push(`«${model.name}» · уверенность ${confidence.toFixed(1)}%`);

  const siteName = extractName(prompt);
  reasoning.push(`Название: «${siteName}»`);

  return {
    siteName,
    siteType,
    industry: best.industry,
    tagline: best.heroSubtitle,
    description: best.heroSubtitle,
    palette: PALETTES[paletteIdx],
    sections: best.sections,
    buttons: best.buttons,
    features: best.features,
    pages: ['Главная', ...best.sections.slice(1).map((s) => s.title), 'Контакты'],
    navItems: ['Главная', 'О нас', 'Услуги', 'Контакты'],
    phone: best.phone,
    address: best.address,
    email: best.email,
    stats: best.stats,
    confidence,
    reasoning,
    heroTitle: best.heroTitle,
    heroSubtitle: best.heroSubtitle,
    ctaText: best.ctaText,
  };
}